public class VariableEnteraApp {

	public static void main(String[] args) {
		
		int n = 4;
		int inc = n + 77;
		int decr = n - 3;
		int dup = n * 2;
		
		System.out.println("Valor inicial: "+n);
		System.out.println("Variable + 77 = "+inc);
		System.out.println("Variable - 3 = "+decr);
		System.out.println("Variable * 2 = "+dup);

	}

}
